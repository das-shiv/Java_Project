public class While {
    public static void main(String[] args) {
        
        int a = 1;

        // while loop

        while(a<=10)
        {
            System.out.println("While Loop counter :" +a);
            a++;
        }

        // do while loop 
        do{
            System.out.println("Do while loop counter :" +a);
            a++;
        } while (a<1);
        



    
}
}
